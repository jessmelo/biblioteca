package Biblioteca;

public class Periodico {
	 private String titulo;
	 private String primeirovolume;
	 private String ultimovolume;
	 private String editora;
	 private int publi_id;
	 
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getPrimeirovolume() {
		return primeirovolume;
	}
	public void setPrimeirovolume(String primeirovolume) {
		this.primeirovolume = primeirovolume;
	}
	public String getUltimovolume() {
		return ultimovolume;
	}
	public void setUltimovolume(String ultimovolume) {
		this.ultimovolume = ultimovolume;
	}
	public String getEditora() {
		return editora;
	}
	public void setEditora(String editora) {
		this.editora = editora;
	}
	public int getPubli_id() {
		return publi_id;
	}
	public void setPubli_id(int publi_id) {
		this.publi_id = publi_id;
	}
	    
	
}
