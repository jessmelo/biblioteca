package Biblioteca;

public class ArtigoLivro {

    private String titulo_artigo;
	private int publi_id;
    private String titulo_livro;
    private String titulo_original;
    private int edicao;
    private int numcapitulo;
    private int ano_publicacao;
    
	public String getTitulo_artigo() {
		return titulo_artigo;
	}
	public void setTitulo_artigo(String titulo_artigo) {
		this.titulo_artigo = titulo_artigo;
	}
	public int getPubli_id() {
		return publi_id;
	}
	public void setPubli_id(int publi_id) {
		this.publi_id = publi_id;
	}
	public String getTitulo_livro() {
		return titulo_livro;
	}
	public void setTitulo_livro(String titulo_livro) {
		this.titulo_livro = titulo_livro;
	}
	public String getTitulo_original() {
		return titulo_original;
	}
	public void setTitulo_original(String titulo_original) {
		this.titulo_original = titulo_original;
	}
	public int getEdicao() {
		return edicao;
	}
	public void setEdicao(int edicao) {
		this.edicao = edicao;
	}
	public int getNumcapitulo() {
		return numcapitulo;
	}
	public void setNumcapitulo(int numcapitulo) {
		this.numcapitulo = numcapitulo;
	}
	public int getAno_publicacao() {
		return ano_publicacao;
	}
	public void setAno_publicacao(int ano_publicacao) {
		this.ano_publicacao = ano_publicacao;
	}
    
}
