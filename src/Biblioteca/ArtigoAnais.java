package Biblioteca;

public class ArtigoAnais {
	private String titulo;
	private String titulocongresso;
	private int numero;
	private int volume;
	private int ano;
	private String mes;
	
    public String getTitulo() {
        return this.titulo;
    }
    public void setTitulo(String novo) {
        this.titulo = novo;
    }
    public String getTituloCongresso() {
        return this.titulocongresso;
    }
    public void setTituloCongresso(String novo) {
        this.titulocongresso = novo;
    }
    public int getNumero() {
        return this.numero;
    }
    public void setNumero(int novo) {
        this.numero = novo;
    }
    public int getVolume() {
        return this.volume;
    }
    public void setVolume(int novo) {
        this.volume = novo;
    }
    public int getAno() {
        return this.ano;
    }
    public void setAno(int novo) {
        this.ano = novo;
    }
    public String getMes() {
        return this.mes;
    }
    public void setMes(String novo) {
        this.mes = novo;
    }

}
