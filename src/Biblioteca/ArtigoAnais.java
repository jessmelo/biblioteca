package Biblioteca;

public class ArtigoAnais {

	private String titulo;
	private int publi_id;
	private String titulocongresso;
	private int numero;
	private int volume;
	private int ano;
	private String mes;
	
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public int getPubli_id() {
		return publi_id;
	}
	public void setPubli_id(int publi_id) {
		this.publi_id = publi_id;
	}
	public String getTitulocongresso() {
		return titulocongresso;
	}
	public void setTitulocongresso(String titulocongresso) {
		this.titulocongresso = titulocongresso;
	}
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public int getVolume() {
		return volume;
	}
	public void setVolume(int volume) {
		this.volume = volume;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public String getMes() {
		return mes;
	}
	public void setMes(String mes) {
		this.mes = mes;
	}
	
}
