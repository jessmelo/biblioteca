package Biblioteca;

public class ArtigoPeriodicos {
	private String titulo;
	private String tituloperiodico;
	private int numvolume;
	private int ano;
	private String mes;
	
    public String getTitulo() {
        return this.titulo;
    }
    public void setTitulo(String novo) {
        this.titulo = novo;
    }
    public String getTituloPeriodico() {
        return this.tituloperiodico;
    }
    public void setTituloPeriodico(String novo) {
        this.tituloperiodico = novo;
    }
    public int getNumVolume() {
        return this.numvolume;
    }
    public void setNumVolume(int novo) {
        this.numvolume = novo;
    }
    public int getAno() {
        return this.ano;
    }
    public void setAno(int novo) {
        this.ano = novo;
    }
    public String getMes() {
        return this.mes;
    }
    public void setMes(String novo) {
        this.mes = novo;
    }


}
